package oit.is.z1976.fgo.fgo.model;

public class Materials {
  String name;
  int amount;

  public void setName(String name) {
    this.name = name;
  }

  public void setAmount(int amount) {
    this.amount = amount;
  }

  public String getName() {
    return name;
  }

  public int getAmount() {
    return amount;
  }
}
